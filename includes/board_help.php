<br><br>Smilies can be added to a post by clicking on the icons.

<br><br>Text color can be change by clicking on appropriate color and by putting the text in between the tags
[color] put your text here [/color] (For example [blue] this text here will be blue [/blue])

<br><br>A link can be added into a post in several ways. If you wish to add a clickable text link like
"http://www.whatever.com" use this code [url]http://www.whatever.com[/url].

<br><br>If you wish to put a link behind a text use this code [url=http://www.whatever.com]click here[/url]

<br><br>Also an email link can be added. For email links use [email]myemail@myserver.com[/email]

<br><br>To add a picture to a post use [img]http://www.images.com/mypicture.jpg[/img]

<br><br>Another feauture is an offtopic comment. For this use [offtopic] offtopic comment [/offtopic].